#include "libft.h"

long long	ft_atoi(const char *str)
{
	int	i;
	long long	res;
	int				neg;

	i = 0;
	res = 0;
	neg = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg = -neg;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + str[i] - '0';
		i++;
	}
	if (neg < 0 && res > 2147483648)
		return (-1);
	else if (res >= 2147483648 && neg > 0)
		return (-1);
	return (res * neg);
}
